

#include <iostream>
#include <string>
using namespace std;

int main() {
    // Declaring and initializing a string variable
    string message = "Hello Engineering World";

    // Displaying the original message
    cout << "Message: " << message << endl;

    // Calculating and displaying the length of the string using built-in function
    cout << "Length of string: " << message.length() << endl;

    return 0;
}
